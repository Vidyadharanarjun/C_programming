#include <stdio.h>
int main ()
{
int a = 10;
printf("Initial a = %d\n\n", a);

printf("output of a++ : %d\n", a++);
printf("value of a now : %d\n", a);

printf("output of ++a : %d\n", ++a);
printf("value of a : %d\n", a);

printf("output of a-- : %d\n", --a);
printf("value of a : %d\n", a);

printf("output of --a : %d\n", --a);
printf("output of a : %d\n", a);
return 0;
}

